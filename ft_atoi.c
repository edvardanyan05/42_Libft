#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	i;
	int	num;
	int	mark;

	i = 0;
	while (nptr[i] == ' ' || nptr[i] == '\n' || nptr[i] == '\t'
		|| nptr[i] == '\v' || nptr[i] == '\f' || nptr[i] == '\r')
		i++;
	mark = 1;
	if (nptr[i] == '-')
	{
		mark = -1;
		i++;
	}
	else if (nptr[i] == '+')
		i++;
	num = 0;
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		num = (num * 10) + (nptr[i] - '0');
		i++;
	}
	return (num * mark);
}
